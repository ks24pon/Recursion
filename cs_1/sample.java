class MyClass{
    public static void main(String[] args){
        // 文字列の出力
        System.out.println("The earth was bluish.");
        // 空文字列出力
        System.out.println("");
        // 整数の出力
        System.out.println(1 + 1);
        // 文字列1同士の出力
        System.out.println("1" + "1");
    }
}
